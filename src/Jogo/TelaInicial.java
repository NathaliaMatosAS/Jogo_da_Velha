    private void botaoINICIARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoINICIARActionPerformed

        
        if(BtnX.getBackground()==Color.green || BtnO.getBackground()==Color.green){
            if(BtnX.getBackground()==Color.green){
                v.j1.setNome(txtJogador1.getText());
                v.j1.setSimbolo("X");
                v.j2.setNome(txtJogador2.getText());
                v.j2.setSimbolo("O");
            }else{
                v.j1.setNome(txtJogador1.getText());
                v.j1.setSimbolo("O");
                v.j2.setNome(txtJogador2.getText());
                v.j2.setSimbolo("X");
            }
                            
                v.turno=0;
                v.passarTurno();
                v.habilitarJogo();
        }else{
            JOptionPane.showMessageDialog(null, "Jogador 1, selecione um símbolo!");
        } 
    }//GEN-LAST:event_botaoINICIARActionPerformed

